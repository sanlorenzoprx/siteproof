export type TranslationKey = string;

export function translate(language: SiteProofLanguage, key: TranslationKey): string {
  const read = (source: unknown): unknown => key.split('.').reduce((value: any, segment) => value?.[segment], source);
  const localized = read(i18n[language]);
  const fallback = read(i18n.en);
  return typeof localized === 'string' ? localized : typeof fallback === 'string' ? fallback : key;
}
